'use client';

import BookCard from './BookCard';

interface Book {
  id: number;
  title: string;
  description: string;
  published_at?: string;
  authors?: Array<{ name: string }>;
}

interface OverviewTabProps {
  books: Book[];
  loading: boolean;
  onBorrow: (book: Book) => void;
  borrowingId: number | null;
}

export default function OverviewTab({ books, loading, onBorrow, borrowingId }: OverviewTabProps) {
  if (loading) {
    return (
      <div className="flex justify-center items-center py-12">
        <div className="animate-spin rounded-full h-12 w-12 border-b-2 border-teal-600"></div>
      </div>
    );
  }

  if (!books || books.length === 0) {
    return (
      <div className="text-center py-12">
        <div className="w-16 h-16 bg-gray-200 rounded-full flex items-center justify-center mx-auto mb-4">
          <svg className="w-8 h-8 text-gray-400" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 6.253v13m0-13C10.832 5.477 9.246 5 7.5 5S4.168 5.477 3 6.253v13C4.168 18.477 5.754 18 7.5 18s3.332.477 4.5 1.253m0-13C13.168 5.477 14.754 5 16.5 5c1.746 0 3.332.477 4.5 1.253v13C19.832 18.477 18.246 18 16.5 18c-1.746 0-3.332.477-4.5 1.253" />
          </svg>
        </div>
        <h3 className="text-lg font-medium text-gray-900 mb-2">No books available</h3>
        <p className="text-gray-500">Check back later for new additions to our collection.</p>
      </div>
    );
  }

  return (
    <div className="px-4 py-6 sm:px-0">
      <div className="mb-6">
        <h2 className="text-2xl font-bold text-gray-900 mb-2">Browse Available Books</h2>
        <p className="text-gray-600">Discover and borrow books from our collection</p>
      </div>

      <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 xl:grid-cols-4 gap-6">
        {books.map((book) => (
          <BookCard
            key={book.id}
            book={book}
            onBorrow={onBorrow}
            isBorrowing={borrowingId === book.id}
          />
        ))}
      </div>
    </div>
  );
}